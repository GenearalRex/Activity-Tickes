    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (seleccionado.showDialog(null,"GUARDAR TEXTO ")==JFileChooser.APPROVE_OPTION){
            archivo=seleccionado.getSelectedFile();
            if (archivo.getName().endsWith("txt")){
                String contenido=txtArea.getText();
                String respuesta=ges.GuaradarATexto(archivo,contenido);
                if(respuesta!=null){
                    JOptionPane.showMessageDialog(null, respuesta);
                }else{
                    JOptionPane.showMessageDialog(null, "Error al guardar el documento.");
                }
            }else{
                JOptionPane.showMessageDialog(null,"Este es unm formato de tipo texto." );
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

